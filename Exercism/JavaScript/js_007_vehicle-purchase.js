/**
 * Determines whether or not you need a license to operate a certain kind of vehicle.
 *
 * @param {string} kind
 * @returns {boolean} whether a license is required
 */
export function needsLicense(kind) {
    if (kind === 'car' || kind === 'truck') {
      return true;  
    } else {
      return false;
    }
  }
  